public class ForLoop {
public static void main(String[] args) {
    



       int d = 0, c = 0;

 for ( d = 1; d <= 7; d++);
       {
           for (c = 500; c <= 1000; c = c + 100) ;
           {
               System.out.println("" + c);
           }

           System.out.println();

       }




    }
}
